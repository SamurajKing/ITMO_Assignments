package hw6_wordstatpp;

public class Pair1 {
    private int first;
    private int second;

    public Pair1(int first, int second) {
        this.first = first;
        this.second = second;
    }

    public int getFirst() {
        return this.first;
    }

    public int getSecond() {
        return this.second;
    }
}
